#include "../includes/libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*destination;
	unsigned char	*source;

	destination = (unsigned char *)dst;
	source = (unsigned char *)src;
	if (src == dst)
		return (dst);
	while (n)
	{
		--n;
		destination[n] = source[n];
	}
	return (dst);
}
